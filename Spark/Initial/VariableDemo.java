class VariableDemo {
    public static void main(String[] args) {
        int age = 10;
        float salary = 10.5f;
        char gender = 'M';
        boolean isStudent = true;
        String name = "John";
        System.out.println(age);
        System.out.println(salary);
        System.out.println(gender);
        System.out.println(isStudent);
        System.out.println(name);
    }
}